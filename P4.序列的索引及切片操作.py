#正向递增索引

s= 'helloworld'
for i in range(0,len(s)):
    print(i,s[i],end='\t\t')
print('\n--------------------------------------------------------------------------------------')

#逆向递减索引
for i in range(-len(s),0):
    print(i,s[i],end='\t\t')

print('\n--------------------------------------------------------------------------------------')
print(s[9],s[-1])


print('\n--------------------------------------------------------------------------------------')
